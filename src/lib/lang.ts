// ============================================================================
// 内容语言判据（lang）—— 零依赖纯函数，供摄入 / 编译 / 导出多层复用
// ----------------------------------------------------------------------------
// 单一事实源：判断一段文本（简历内容）是中文还是英文。
// 铁律背景：master/version 的 language 字段【必须】由真实内容检测得出，绝不硬编码——
// 否则英文简历的 language 恒为 "zh"，是不诚实的死字段，且会让导出混入中文 token、
// 阻断未来按语言分流的能力（双母版、导出文案等）。
// ============================================================================

/** 内容语言判据：中文字符多于拉丁字母 → zh，否则 en。
 *  对简历这类文本足够稳：中文母版 CJK 远多于拉丁，英文母版几乎无 CJK；
 *  少量混入的中文（如个别工具名）不会把英文简历误判为中文。 */
export function detectContentLang(text: string): "zh" | "en" {
  const cjk = (text.match(/[一-鿿]/g) ?? []).length;
  const latin = (text.match(/[A-Za-z]/g) ?? []).length;
  return cjk > latin ? "zh" : "en";
}
